export const TEAM_SETUP_GUIDE = `
## Setting Up Your Team

### 1. Create a Team
Click the team switcher in the top-left of the sidebar. Select **Create Team** and give it a name. You will automatically be the team admin.

### 2. Invite Members
After creating your team, click **Invite Member** in the sidebar. Enter the email address of each person you want to invite. They will receive an email with a link to join.

### 3. Manage Roles
Team members can have two roles:
- **Admin**: Can manage team settings, invite/remove members, create sub-teams, and access the Admin Panel.
- **Member**: Can create and manage tasks, comment, and use chat.

To change a member's role, go to the **Admin Panel** and click on the member's current role.

### 4. Create Sub-Teams
Sub-teams help organize your team into smaller groups (e.g., Design, Engineering, Marketing). Go to the **Admin Panel > Sub-Teams** tab to create sub-teams and assign members.

### 5. Set Up Projects
Create projects to organize your work. Click the **+** button next to "Projects" in the sidebar. Give the project a name, optional emoji, and description.

### 6. Organize with Folders
Projects can be nested inside folders for better organization. Create a folder from the project list and drag projects into it.

### 7. Configure Permissions
By default, all team members can access all projects. Use the Admin Panel to configure granular permissions if you need to restrict access to specific projects or features.

### 8. Best Practices
- Start with one or two projects and expand as your team gets comfortable.
- Use task types (Ad-Hoc, Project, Recurring) to categorize work.
- Encourage team members to use the chat feature for quick discussions.
- Set up recurring tasks for regular team activities (standups, reviews, etc.).
- Use the Kanban board view for visual workflow management.
- Review the Trash page periodically to permanently clean up old tasks.
`
